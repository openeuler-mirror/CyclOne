// RemoveServerRoomByID 移除机房 /api/cloudboot/v1/server-room/{id}
func RemoveServerRoomByID(w http.ResponseWriter, r *http.Request) {
	repo, _ := middleware.RepoFromContext(r.Context())
	log, _ := middleware.LoggerFromContext(r.Context())

	id, err := strconv.Atoi((chi.URLParam(r, "id")))
	if err != nil {
		log.Error(err)
		render.JSON(w, http.StatusBadRequest, myhttp.ErrRespBody(err.Error()))
		return
	}

	//删除前校验，存在子数据不允许删除
	msg := service.RemoveServerRoomValidte(log, repo, uint(id))
	if msg != "" {
		render.JSON(w, http.StatusBadRequest, myhttp.ErrRespBody(msg))
		return
	}

	err = service.RemoveServerRoomByID(log, repo, uint(id))
	if err != nil {
		HandleErr(r.Context(), w, err)
		return
	}
	render.JSON(w, http.StatusOK, myhttp.SucceedRespBody("操作成功"))
}

